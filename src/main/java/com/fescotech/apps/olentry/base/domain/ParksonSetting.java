package com.fescotech.apps.olentry.base.domain;

public class ParksonSetting {
	
	private Integer householdArea;
	private Integer householdType;
	private Integer personType;
	private Integer postType;
	public Integer getHouseholdArea() {
		return householdArea;
	}
	public void setHouseholdArea(Integer householdArea) {
		this.householdArea = householdArea;
	}
	public Integer getHouseholdType() {
		return householdType;
	}
	public void setHouseholdType(Integer householdType) {
		this.householdType = householdType;
	}
	public Integer getPersonType() {
		return personType;
	}
	public void setPersonType(Integer personType) {
		this.personType = personType;
	}
	public Integer getPostType() {
		return postType;
	}
	public void setPostType(Integer postType) {
		this.postType = postType;
	}

}
